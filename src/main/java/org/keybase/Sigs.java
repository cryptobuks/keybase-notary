package org.keybase;

public class Sigs {
	private String sig;
	private String fingerprint;
	
	   public String getSig() {
	        return sig;
	    }
	    public void setSig(String sig) {
	        this.sig = sig;
	    }
	    public String getFingerprint() {
	        return fingerprint;
	    }
	    public void setFingerprint(String fingerprint) {
	        this.fingerprint = fingerprint;
	    }
}
